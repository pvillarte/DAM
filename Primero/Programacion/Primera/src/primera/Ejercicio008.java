package primera;
//x++ Autoincrementativo, x-- autodecrementativo
public class Ejercicio008 {
    public static void main (String arg[]){
        int cont;     //Definimos un contador para las iteraciones del bucle
        for (cont=0; cont <10; cont++ ) //Valor inicial, condición salida, paso
            System.out.println("Iteración: "+(cont+1));                             
    }
    
}
